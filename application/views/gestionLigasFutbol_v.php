    <p><span class="negrita">Gestión Integral de Campeonatos</span> </p>
    	 
Esta aplicación es una herramienta de software pensada para el seguimiento de los campeonatos, cuyo objetivo principal es el de ayudar a la gestión de las distintas divisiones, integrando, verificando y retroalimentando la información de los partidos. Las principales características del mismo  son

    Brindar información sobre los campeonatos con sus  respectivas divisiones.
    Brindar información estadístita sobre lo acontecido en cada uno de los partidos de fútbol.
    Posibilitar el acceso vía Web a la información que gestiona el sistema, mediante el control de acceso a la información dependiendo del tipo de usuario.

a aplicación de software Gestión Integral de Campeonatos posee las siguientes características:

    Multiusuario
    Trabajo en Red
    Acceso vía WEB
    Integración automática entre las aplicaciones
    Estandarización de pantallas, consultas e informes
    Generación de pantallas orientadas por el usuario

Los requerimientos básicos para la utilización de la aplicación son:

    Sistema Operativo Windows XP
    Procesador PENTIUM IV o más
    Memoria 512 MB RAM o más recomendados
    Espacio en disco duro 80 MB mínimo

La aplicación implementa mecanismos que permiten gestionar los diferentes campeonatos. 

